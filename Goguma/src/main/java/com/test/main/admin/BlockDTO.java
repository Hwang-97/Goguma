package com.test.main.admin;

public class BlockDTO {

	private String id;
	private String blockTypeSeq;
	private String regDate;
	private String type;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBlockTypeSeq() {
		return blockTypeSeq;
	}
	public void setBlockTypeSeq(String blockTypeSeq) {
		this.blockTypeSeq = blockTypeSeq;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
